# Импортируем класс Flask из одноимённой библиотеки.
# Flask — это микрофреймворк для создания веб-приложений.
from flask import Flask, render_template, request

# Импортируем функцию pipeline из библиотеки transformers (Hugging Face).
# Pipeline — это высокоуровневый инструмент, который упрощает использование
# предобученных моделей NLP (токенизация + модель + постобработка в одну строку).
from transformers import pipeline


# ============================================================
# БЛОК 2: СОЗДАНИЕ ЭКЗЕМПЛЯРА FLASK-ПРИЛОЖЕНИЯ
# ============================================================

# Создаём объект приложения Flask.
# __name__ — это специальная переменная Python, которая содержит имя текущего модуля.
# Flask использует её для определения расположения статических файлов и шаблонов.
app = Flask(__name__)


# ============================================================
# БЛОК 3: ЗАГРУЗКА МОДЕЛИ МАШИННОГО ОБУЧЕНИЯ
# ============================================================

# Оборачиваем загрузку модели в блок try-except для обработки возможных ошибок.
# Это нужно, чтобы приложение не падало целиком, если модель недоступна.
try:
    # Создаём конвейер (pipeline) для анализа тональности (sentiment-analysis).
    # Параметр "sentiment-analysis" указывает задачу, которую будет решать pipeline.
    # Параметр model="blanchefort/rubert-base-cased-sentiment" задаёт конкретную модель:
    #   - rubert-base-cased — базовая архитектура BERT для русского языка,
    #   - sentiment — модель дообучена для задачи анализа эмоций,
    #   - cased — модель различает заглавные и строчные буквы.
    # Hugging Face автоматически скачивает модель при первом запуске и кэширует её.
    sentiment_analyzer = pipeline(
        "sentiment-analysis",
        model="blanchefort/rubert-base-cased-sentiment"
    )

    # Создаём словарь MOVIE_RECOMMENDATIONS, который содержит подборки фильмов
    # для каждого возможного результата анализа (POSITIVE, NEGATIVE, NEUTRAL).
    # Структура словаря: ключ -> значение (ещё один словарь с полями title и movies).
    MOVIE_RECOMMENDATIONS = {
        # Ключ "POSITIVE" соответствует позитивному настроению.
        "POSITIVE": {
            # Заголовок рекомендации, который увидит пользователь.
            "title": "Вам рекомендуется комедия или приключенческий фильм",
            # Список фильмов (три строки с номерами, названиями и пояснениями).
            "movies": [
                "1. 1+1 (2011) - трогательная комедия о дружбе",
                "2. В поисках Немо (2003) - веселое подводное приключение",
                "3. Реальная любовь (2003) - романтическая комедия"
            ]
        },
        # Ключ "NEGATIVE" соответствует негативному настроению.
        "NEGATIVE": {
            "title": "Вам рекомендуется вдохновляющая драма или уютная комедия",
            "movies": [
                "1. Все будет хорошо (2015) - история о преодолении трудностей",
                "2. Шоколад (2000) - теплый фильм, поднимающий настроение",
                "3. Дневник памяти (2004) - романтическая драма о надежде"
            ]
        },
        # Ключ "NEUTRAL" соответствует нейтральному настроению.
        "NEUTRAL": {
            "title": "Вам рекомендуется интеллектуальное или познавательное кино",
            "movies": [
                "1. Начало (2010) - захватывающий научно-фантастический триллер",
                "2. Зеленая книга (2018) - драма с глубоким смыслом",
                "3. Король говорит! (2010) - историческая драма о преодолении"
            ]
        }
    }

    # Выводим в консоль сервера сообщение об успешной загрузке модели.
    # Это полезно для отладки — администратор видит, что всё работает.
    print("Модель успешно загружена!")

# Блок except выполняется, если при попытке загрузить модель произошла ошибка.
except Exception as e:
    # Выводим в консоль сообщение об ошибке с текстом исключения.
    print(f"Ошибка загрузки модели: {e}")
    # Устанавливаем sentiment_analyzer = None, чтобы последующий код знал,
    # что модель недоступна, и мог корректно обработать эту ситуацию.
    sentiment_analyzer = None


# ============================================================
# БЛОК 4: ОПРЕДЕЛЕНИЕ МАРШРУТА (ROUTE) И ФУНКЦИИ-ОБРАБОТЧИКА
# ============================================================

# Декоратор @app.route связывает URL-адрес с функцией, которая будет его обрабатывать.
# Аргумент '/' означает корневой URL (например, http://localhost:5000/).
# Аргумент methods=["GET", "POST"] указывает, что функция принимает оба типа HTTP-запросов:
#   - GET — используется, когда пользователь просто открывает страницу,
#   - POST — используется, когда пользователь отправляет форму (нажимает кнопку).
@app.route('/', methods=["GET", "POST"])
def index():
    """Функция-обработчик главной страницы. Выполняется при каждом обращении к '/'."""

    # Инициализируем пустые переменные для данных, которые будут переданы в шаблон.
    # recommendation_text — текст с определённым настроением (например, "У вас позитивное настроение!").
    recommendation_text = ""
    # recommendation_title — заголовок подборки фильмов (берётся из MOVIE_RECOMMENDATIONS).
    recommendation_title = ""
    # movies_list — список фильмов для отображения.
    movies_list = []
    # user_text — текст, который пользователь ввёл в поле ввода (чтобы подставить его обратно).
    user_text = ""

    # Проверяем тип HTTP-запроса.
    # Если request.method == "POST", значит пользователь отправил форму.
    if request.method == "POST":
        # Извлекаем текст сообщения из отправленной формы.
        # request.form — словарь с данными формы (передаётся методом POST).
        # Ключ "message" соответствует атрибуту name="message" в HTML-поле <textarea>.
        user_text = request.form["message"]

        # Проверяем, загружена ли модель (не равна ли None).
        if sentiment_analyzer is None:
            # Если модель не загружена, формируем сообщение об ошибке.
            recommendation_text = "Модель не загружена. Попробуйте позже."
        else:
            # Если модель загружена, передаём ей текст пользователя для анализа.
            # sentiment_analyzer(user_text) возвращает список результатов.
            # [0] — берём первый (и единственный) элемент списка.
            # Результат — словарь с ключами "label" и "score".
            result = sentiment_analyzer(user_text)[0]
            # Извлекаем метку класса (POSITIVE, NEGATIVE или NEUTRAL).
            label = result["label"]

            # Ветвление в зависимости от полученной метки.
            if label == "POSITIVE":
                # Для позитивного настроения:
                recommendation_text = "У вас позитивное настроение!"
                recommendation_title = MOVIE_RECOMMENDATIONS["POSITIVE"]["title"]
                movies_list = MOVIE_RECOMMENDATIONS["POSITIVE"]["movies"]

            elif label == "NEGATIVE":
                # Для негативного настроения:
                recommendation_text = "Кажется, вы немного грустите."
                recommendation_title = MOVIE_RECOMMENDATIONS["NEGATIVE"]["title"]
                movies_list = MOVIE_RECOMMENDATIONS["NEGATIVE"]["movies"]

            else:
                # Для нейтрального настроения (label == "NEUTRAL"):
                recommendation_text = "У вас нейтральное настроение."
                recommendation_title = MOVIE_RECOMMENDATIONS["NEUTRAL"]["title"]
                movies_list = MOVIE_RECOMMENDATIONS["NEUTRAL"]["movies"]

    # После обработки GET или POST запроса рендерим HTML-шаблон.
    # render_template() ищет файл "index.html" в папке "templates" (рядом со скриптом).
    # Передаём в шаблон переменные, которые будут подставлены в нужные места.
    return render_template("index.html",
                           recommendation_text=recommendation_text,
                           recommendation_title=recommendation_title,
                           movies_list=movies_list,
                           user_text=user_text)


# ============================================================
# БЛОК 5: ЗАПУСК ВЕБ-СЕРВЕРА
# ============================================================

# Это стандартная конструкция Python:
# if __name__ == '__main__' проверяет, что скрипт запущен напрямую,
# а не импортирован как модуль в другой скрипт.
if __name__ == '__main__':
    # Запускаем встроенный веб-сервер Flask.
    # Параметр debug=True включает режим отладки:
    #   - автоматическая перезагрузка сервера при изменении кода,
    #   - подробные сообщения об ошибках в браузере (удобно при разработке).
    # ВАЖНО: debug=True НЕ используется в production (боевой среде).
    app.run(debug=True)